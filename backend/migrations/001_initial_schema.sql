-- SIEMBox Initial Database Schema
-- This file contains all the tables needed for the SIEM system

-- Enable pgcrypto extension for cryptographic functions (MD5, SHA256)
CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- Users table for authentication and access control
CREATE TABLE IF NOT EXISTS users (
    id SERIAL PRIMARY KEY,
    username VARCHAR(255) NOT NULL UNIQUE,
    email VARCHAR(255) NOT NULL UNIQUE,
    password_hash VARCHAR(255) NOT NULL,
    role VARCHAR(50) NOT NULL DEFAULT 'viewer', -- 'admin', 'analyst', 'viewer', 'operator'
    enabled BOOLEAN DEFAULT true,
    last_login TIMESTAMP,
    created_at TIMESTAMP DEFAULT NOW(),
    updated_at TIMESTAMP DEFAULT NOW()
);

-- Sessions table for session management
CREATE TABLE IF NOT EXISTS sessions (
    id SERIAL PRIMARY KEY,
    user_id INTEGER REFERENCES users(id) ON DELETE CASCADE,
    token VARCHAR(255) NOT NULL UNIQUE,
    expires_at TIMESTAMP NOT NULL,
    created_at TIMESTAMP DEFAULT NOW()
);

-- Raw syslog messages as received
CREATE TABLE IF NOT EXISTS raw_logs (
    id SERIAL PRIMARY KEY,
    timestamp TIMESTAMP NOT NULL,
    raw_message TEXT NOT NULL,
    source_ip VARCHAR(45) NOT NULL,
    facility INTEGER,
    severity INTEGER,
    hostname VARCHAR(255),
    app_name VARCHAR(255),
    shipper_id VARCHAR(50), -- Short identifier from log shipper for tracing logs back to source
    created_at TIMESTAMP DEFAULT NOW()
);

-- Parsers for transforming raw logs into structured data
CREATE TABLE IF NOT EXISTS parsers (
    id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL UNIQUE,
    description TEXT,
    enabled BOOLEAN DEFAULT true,
    priority INTEGER DEFAULT 100, -- Lower number = higher priority
    parser_type VARCHAR(50) NOT NULL, -- 'regex', 'grok', 'json'
    pattern TEXT NOT NULL,
    field_mappings JSONB NOT NULL, -- Maps pattern groups to field names
    test_samples JSONB, -- Sample logs for testing
    event_type VARCHAR(100), -- Optional: event type for parsed logs (e.g., 'ssh_failed_login', 'http_request')
    created_at TIMESTAMP DEFAULT NOW(),
    updated_at TIMESTAMP DEFAULT NOW()
);

-- Parsed logs with structured data
CREATE TABLE IF NOT EXISTS parsed_logs (
    id SERIAL PRIMARY KEY,
    raw_log_id INTEGER REFERENCES raw_logs(id) ON DELETE CASCADE,
    parser_id INTEGER REFERENCES parsers(id) ON DELETE SET NULL,
    parsed_data JSONB NOT NULL,
    timestamp TIMESTAMP NOT NULL,
    source_ip VARCHAR(45) NOT NULL,
    event_type VARCHAR(100), -- e.g., 'ssh_login', 'http_request', 'firewall_block'
    created_at TIMESTAMP DEFAULT NOW()
);

-- Detection rules for identifying security events
CREATE TABLE IF NOT EXISTS detection_rules (
    id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL UNIQUE,
    description TEXT,
    enabled BOOLEAN DEFAULT true,
    severity VARCHAR(20) NOT NULL, -- 'low', 'medium', 'high', 'critical'
    rule_yaml TEXT NOT NULL,
    rule_logic JSONB NOT NULL, -- Parsed YAML for quick access
    tags TEXT[], -- For categorization ['ssh', 'brute-force', 'authentication']
    created_at TIMESTAMP DEFAULT NOW(),
    updated_at TIMESTAMP DEFAULT NOW()
);

-- Alerts generated by detection rules
CREATE TABLE IF NOT EXISTS alerts (
    id SERIAL PRIMARY KEY,
    rule_id INTEGER REFERENCES detection_rules(id) ON DELETE CASCADE,
    parsed_log_id INTEGER REFERENCES parsed_logs(id) ON DELETE SET NULL,
    severity VARCHAR(20) NOT NULL,
    title VARCHAR(255) NOT NULL,
    description TEXT,
    matched_data JSONB NOT NULL, -- Data that matched the rule
    status VARCHAR(20) DEFAULT 'new', -- 'new', 'investigating', 'closed', 'false_positive'
    assigned_to INTEGER REFERENCES users(id) ON DELETE SET NULL,
    created_at TIMESTAMP DEFAULT NOW(),
    updated_at TIMESTAMP DEFAULT NOW()
);

-- IP whitelist for admin access control and rule exceptions
CREATE TABLE IF NOT EXISTS ip_whitelist (
    id SERIAL PRIMARY KEY,
    ip_address CIDR NOT NULL,
    description TEXT,
    rule_id VARCHAR(50),
    created_by INTEGER REFERENCES users(id),
    created_at TIMESTAMP DEFAULT NOW(),
    updated_at TIMESTAMP DEFAULT NOW()
);

