-- Triggers for auto-calculation

-- Auto-calculate scan duration
CREATE OR REPLACE FUNCTION update_scan_duration()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.completed_at IS NOT NULL AND NEW.started_at IS NOT NULL THEN
        NEW.duration_seconds := EXTRACT(EPOCH FROM (NEW.completed_at - NEW.started_at));
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_scan_duration
BEFORE UPDATE ON vulnerability_scans
FOR EACH ROW
WHEN (NEW.completed_at IS NOT NULL)
EXECUTE FUNCTION update_scan_duration();

-- Auto-calculate risk score (criticality × CVSS)
CREATE OR REPLACE FUNCTION calculate_risk_score()
RETURNS TRIGGER AS $$
DECLARE
    asset_crit TEXT;
    cvss NUMERIC;
    multiplier NUMERIC;
BEGIN
    SELECT criticality INTO asset_crit FROM assets WHERE id = NEW.asset_id;
    SELECT cvss_score INTO cvss FROM vulnerabilities WHERE id = NEW.vulnerability_id;

    multiplier := CASE asset_crit
        WHEN 'critical' THEN 1.5
        WHEN 'high' THEN 1.2
        WHEN 'medium' THEN 1.0
        WHEN 'low' THEN 0.8
        ELSE 1.0
    END;

    NEW.risk_score := LEAST(10.0, cvss * multiplier);
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_risk_score
BEFORE INSERT OR UPDATE ON asset_vulnerabilities
FOR EACH ROW
EXECUTE FUNCTION calculate_risk_score();

-- Materialized view for dashboard performance
CREATE MATERIALIZED VIEW IF NOT EXISTS dashboard_vulnerability_summary AS
SELECT
    COUNT(*) FILTER (WHERE av.status = 'open' AND v.severity = 'critical') as critical_open,
    COUNT(*) FILTER (WHERE av.status = 'open' AND v.severity = 'high') as high_open,
    COUNT(*) FILTER (WHERE av.status = 'open' AND v.severity = 'medium') as medium_open,
    COUNT(*) FILTER (WHERE av.status = 'open' AND v.severity = 'low') as low_open,
    COUNT(DISTINCT av.asset_id) as affected_assets
FROM asset_vulnerabilities av
JOIN vulnerabilities v ON av.vulnerability_id = v.id
WHERE av.status = 'open';

CREATE UNIQUE INDEX IF NOT EXISTS idx_dashboard_summary ON dashboard_vulnerability_summary((true));

-- Refresh function (call from backend every 5 minutes)
CREATE OR REPLACE FUNCTION refresh_dashboard_summary()
RETURNS void AS $$
BEGIN
    REFRESH MATERIALIZED VIEW CONCURRENTLY dashboard_vulnerability_summary;
END;
$$ LANGUAGE plpgsql;

